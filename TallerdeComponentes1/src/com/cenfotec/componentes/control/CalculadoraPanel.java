        private void escribirNumero(String num) {
        if (!encendida) return;
        if (nuevaOperacion || jTextField1.getText().equals("0")) {
            jTextField1.setText(num);
            nuevaOperacion = false;
        } else {
            jTextField1.setText(jTextField1.getText() + num);
        }
    }

    private void seleccionarOperacion(String op) {
        if (!encendida) return;
        primerNumero = Double.parseDouble(jTextField1.getText());
        operacion = op;
        jTextField1.setText(op); // Muestra el sÃ­mbolo
        nuevaOperacion = true;
    }
    
