    private void jButton12MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton12MouseClicked
        if (!encendida || operacion.isEmpty()) return;

        double segundoNumero = Double.parseDouble(jTextField1.getText());
        double resultado = 0;

        switch (operacion) {
            case "+" -> resultado = primerNumero + segundoNumero;
            case "-" -> resultado = primerNumero - segundoNumero;
            case "x" -> resultado = primerNumero * segundoNumero;
            case "/" -> {
                if (segundoNumero != 0) resultado = primerNumero / segundoNumero;
                else { jTextField1.setText("Error"); return; }
            }
        }

        // Mostrar resultado (quitando el .0 si es entero)
        if (resultado % 1 == 0) {
            jTextField1.setText(String.valueOf((int)resultado));
        } else {
            jTextField1.setText(String.valueOf(resultado));
        }
        operacion = "";
        nuevaOperacion = true;
    }//GEN-LAST:event_jButton12MouseClicked


